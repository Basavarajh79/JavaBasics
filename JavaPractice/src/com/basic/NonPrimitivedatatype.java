package com.basic;

public class NonPrimitivedatatype {
	public static void main(String[] args) {
		
		String s1="java 123@a";
		System.out.println(s1);
		double a=45.36;
		System.out.println(a);
		
		//reinitialization
		a=63.24;
		System.out.println(a);
		a=633.24;
		System.out.println(a);
		a=7.3;
		System.out.println(a);
		
		final int id=46;
		System.out.println(id);
		//id=47;
		System.out.println(id);
		
		
	}

}
