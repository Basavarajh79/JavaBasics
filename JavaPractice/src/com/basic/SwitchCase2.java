package com.basic;

public class SwitchCase2 {
	public static void main(String[] args) {
		//char a='c';
		String s1="a";
		{
			switch(s1)
			{
			case "97":
			System.out.println("10");
			break;
			case "98":
			System.out.println("20");
			break;
			case "99":
			System.out.println("30");
			break;
			case "100":
			System.out.println("40");
			break;
			default:
			System.out.println("invalid");
			break;
			}
		}
	}

}
